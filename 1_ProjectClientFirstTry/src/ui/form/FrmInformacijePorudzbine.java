    private void prepareView(Porudzbina porudzbina) {
        
        jTextFieldID.setText(String.valueOf(porudzbina.getId()));
        jTextAreaOpis.setText(porudzbina.getOpis());
        SimpleDateFormat dateFormat=new SimpleDateFormat("dd.mm.yyyy.");
        jTextFieldDatum.setText(dateFormat.format(porudzbina.getDatum()));
        
        
        jTextFieldIDK.setText(String.valueOf(porudzbina.getKlijent().getId()));
        jTextFieldImeK.setText(porudzbina.getKlijent().getIme());
        jTextFieldPrezimeK.setText(porudzbina.getKlijent().getPrezime());
        jTextFieldKompanijaK.setText(porudzbina.getKlijent().getPreduzece().toString());
        jTextFieldMBK.setText(porudzbina.getKlijent().getMaticni_broj());
        
        
        jTextFieldZaposleni.setText(porudzbina.getZaposleni().toString());
        
        
        
        jTextFieldID.setEnabled(false);
        jTextFieldIDK.setEnabled(false);
        jTextFieldZaposleni.setEnabled(false);
        
        
        
        
     
        jTextAreaOpis.setEnabled(false);
        jTextFieldDatum.setEditable(false);
        jTextFieldImeK.setEnabled(false);
        jTextFieldPrezimeK.setEnabled(false);
        jTextFieldKompanijaK.setEnabled(false);
        jTextFieldMBK.setEnabled(false);
        
        
        
        
    }

    private void izmeni() {
        jTextAreaOpis.setEnabled(true);
        jTextFieldDatum.setEditable(true);
        jTextFieldImeK.setEnabled(true);
        jTextFieldPrezimeK.setEnabled(true);
        jTextFieldKompanijaK.setEnabled(true);
        jTextFieldMBK.setEnabled(true);
    }





