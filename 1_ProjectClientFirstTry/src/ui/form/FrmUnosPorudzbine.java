    private void jButtonSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSacuvajActionPerformed
      try{
                                                           
            Porudzbina porudzbina = new Porudzbina();
            porudzbina.setOpis(jTextAreaOpisUsluge.getText());
            
            String datumStr=jTextFieldDatum.getText();
            SimpleDateFormat dateFormat = new SimpleDateFormat("dd.mm.yyyy.");
            java.util.Date datum = null;
            
            try{
            datum=dateFormat.parse(datumStr);
            porudzbina.setDatum(datum);
            }catch (ParseException ex){
                throw  ex;
            }
            
            
            porudzbina.setZaposleni((Zaposleni) jComboBoxZaduzenja.getSelectedItem());
            
            porudzbina.setKlijent((Klijent) jComboBoxKlijenti.getSelectedItem());
           
            
             Controller.getInstance().kreirajPorudzbinu(porudzbina);
             JOptionPane.showMessageDialog(this, "Sistem je zapamtio porudžbinu!\n", "Unesite porudžbinu", JOptionPane.INFORMATION_MESSAGE);
             this.getTopLevelAncestor().setVisible(false);
        }catch(Exception e){
             JOptionPane.showMessageDialog(this, "Sistem ne može da zapamti porudžbinu!\n","Unesite porudžbinu", JOptionPane.ERROR_MESSAGE);
        }
      
        
        
        
    }//GEN-LAST:event_jButtonSacuvajActionPerformed


