    private void prepareView() throws Exception {


        fillWithZaposleni();
        fillWithKlijenti();
        




    }

    private void fillWithZaposleni() throws Exception {
         List<Zaposleni> zaposleni = Controller.getInstance().getZaposleni();
        for(Zaposleni z:zaposleni){
            jComboBoxZaduzenja.addItem(z);
        }
    }

    private void fillWithKlijenti() throws Exception {
         List<Klijent> klijenti = Controller.getInstance().getKlijenti();
        for(Klijent k:klijenti){
            jComboBoxKlijenti.addItem(k);
        }
    }
